<h3>Definitions 🔭</h3><br>

<h4>Exoplanet</h4> 
<p>An exoplanet is a planet that exists outside of our solar system 🌍✨, orbiting a star other than the Sun. These planets can vary greatly in size, composition, and distance from their host stars, meaning some may be rocky like Earth (Gliese 667 Cc) , while others could be gas giants like Jupiter (Kepler-170-4b) 🪐.</p>

<h4>Star</h4> 
<p>A star is a massive, luminous sphere of hot gas 🌟, primarily hydrogen and helium, held together by its own gravity. At its core, nuclear fusion occurs, where hydrogen atoms combine to form helium, releasing a tremendous amount of energy ⚡ in the form of light and heat. This process is what powers the star and makes it shine. ☀️</p>


<h4>Reference System</h4> 
<p>A reference system is a way to measure and describe the position or movement of objects in space 🌌. It includes a set of points or axes 📏 that helps to understand how different things relate to each other 🔗. Reference systems are important in fields like physics ⚛️ and geography 🗺️ for organizing and interpreting information!</p>
